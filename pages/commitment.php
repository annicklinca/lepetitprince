       <h3 style="font-weight: bold;"> Our Commitment</h3>
<p>For the past year,though there've occured the pandemic of Covid-19, Ecole Le Petit Prince Bilingue has played an important role in providing support for children  with physical disabilities. Thanks to our generous donors and various support from many families, we continue to offer programs that allow kids to experience freedom, independence and a sense of accomplishment. For families, Ecole Le Petit Prince Bilingue is somewhere to turn for help when they need it.

Ecole Bilingue Le Petit Prince  is a registered charitable school center  that helps children a with physical disabilities from all ethnic,religious backgrounds and also from both noble and poor families. We assist families with the purchase of essential mobility and accessibility equipment such as wheelchairs, walkers, porch lifts and ramps, toileting aids and bathing equipment.Ecole Le Petit Prince Bilingue also help the children by teaching them many skills and also assisting their families. 
<br>
<br>
Ecole Bilingue Le Petit Prince helps kids BE KIDS.</p>


<h3 style="font-weight: bold;">MISSION</h3>

<p>
Ecole Bilingue Le Petit Prince is committed to encouraging parents to bring their children with disabilities at our school center and also supporting parents by offering them both our advices and some financial help.Note that ,as we are the only  single center for disabilities in the whole Country, we have to fight against discrimination . we are also ready to provide to these children with disabilities by training them with specialized classes ,so in one word we are fighting for an effective inclusive education which is our main aim.<br>
Ecole Bilingue Le Petit Prince  is also committed to helping children, youth and young adults with physical disabilities and their families. Through the generous support of our donors and volunteers,Ecole Bilingue Le Petit Prince  provides quality services and programs and acts as an effective voice for their needs and interests.  </p>
<br><br>
<h3 style="font-weight: bold">VISION</h3>

<p>Ecole Bilingue Le Petit Prince will take care and train the children, youth and young adults the profession work which will help them to create their own jobs and even get jobs.<br>
  Ecole Bilingue Le Petit Prince will enable all children, youth and young adults with physical disabilities to reach their full potential make the absolute most of their abilities and live their lives to the fullest.<br>
</p>
 

