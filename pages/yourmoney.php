<h1 style="font-weight: bold;">How Your Money Helps</h1>
<h3 style="font-weight: bold;">The Need</h3>
<h3 style="font-weight: bold;">Financial</h3>

<p>Children with physical disabilities require costly equipment to meet basic needs, live safely, and strive for independence. The families of these children face not only personal sacrifice, but also a heavy financial burden. It can cost families more than $40,000 a year to care for a child with a severe disability, and as children grow, equipment must be replaced. Government agencies cover only partial costs of certain pieces of equipment necessary to these children for personal safety, mobility, and communication, and for some pieces, not at all.<br>

Extra family expenses can include: home renovations, adaptive equipment and devices, special schooling, special transportation and adapted vehicles, time away from work for medical appointments and surgeries, plus the cost of care givers, to name a few.<br>
</p>

<h3 style="font-weight: bold;">Social</h3>

<p>Often children with disabilities live with feelings of isolation. Many spend most of their time at home or at school, with very little social interaction with others their age. For many of these children, camp or other recreational experiences are their only chance to establish independence, expand their personal boundaries, and explore their abilities.<br>

All young people, whether disabled or not, benefit from camping or recreational experiences. A recreational environment builds social skills, independence and self-esteem. However, for a child with a physical disability, this experience is so much more</p><br>

<h3 style="font-weight: bold;">Sources of Revenue</h3>
<p>Ecole Bilingue Le Petit Prince relies fully on the generosity of public donations to offer its programs and services. and receives negligible government funding.</p>
<br>
<h3 style="font-weight: bold;">Impact</h3>
<p>The children we serve have lifelong disabilities. Our programs and services, funded through donor contributions and sponsorships, have a positive impact on improving their quality of life and helping them reach their individual potential.</p><br>
