      <b>Our Work with Children Living with Disabilities</b><br>
<p>Disability can mean anything from poor eyesight, to an inability to walk, to complex intellectual or mental health challenges. But any condition can be overcome when the community supports education for people living with disabilities.<br>
In the regions where we work, people living with disabilities face a number of common barriers:
disability.

-People often view disability as a curse or punishment by divine powers.<br>
-Families can be embarrassed by disability, and keep children and/or relatives with disabilities at home.<br>
-Many people living with disabilities face neglect and abuse in their household.<br>
-Poverty means the needs of ‘healthy’ children are prioritised.<br>
-Conflict increases the number of people with disabilities, while decreasing the available support.<br>
-Lack of care and medical support to treat and overcome certain disabilities.<br><br>
All of these challenges make accessing education extremely difficult. Negative attitudes combined with poverty and a lack of support creates a situation where:

Families do not send children with disabilities to school.
Children living with disabilities are thought incapable of learning and ignored in classrooms.
Teachers do not know how to incorporate children living with disabilities into their classes.
Schools lack the resources and infrastructure to accommodate the learning needs of children living with disabilities.
Lack of transportation to schools or classes for those with physical disabilities.
Fewer than 5% of adults living with disabilities across Africa are able to read or write. People living with disabilities deserve, need and want the chance to fully participate in and contribute to their community, and education is an important step in helping them do so.</p>
<br>
 

<p><b>Including Children Living with Disabilites in Education</b>
  <br>
Ecole Le Petit Prince Bilingue is working to provide tailored and flexible education programmes for children living with disabilities. These include:<br>

-Promoting the inclusion of children living with disabilities into regular classrooms and schools.<br>
-deafboy1Providing support so that children can access learning tools or classes to meet their learning needs.<br>
-Parents working with teachers to identify children living with disabilities and get them assistance rather than leaving them to drop out of school.<br>
.</p>
      
   
